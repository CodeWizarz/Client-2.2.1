package com.rapidesuite.snapshot.model;

public class KBVersionInformation {

	private String kbVersion;
	private String kbFileName;
	
	public String getKbVersion() {
		return kbVersion;
	}
	public void setKbVersion(String kbVersion) {
		this.kbVersion = kbVersion;
	}
	public String getKbFileName() {
		return kbFileName;
	}
	public void setKbFileName(String kbFileName) {
		this.kbFileName = kbFileName;
	}

}

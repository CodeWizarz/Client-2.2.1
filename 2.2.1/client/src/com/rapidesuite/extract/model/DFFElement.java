package com.rapidesuite.extract.model;

public class DFFElement {

	private String prompt;
	private String contextCode;
	private String segmentCode;
	private String columnName;
	private String sequenceNumber;
	private String valueSetCode;
	private String validationType;
	private String valueDataType;
	
	public String getPrompt() {
		return prompt;
	}
	public void setPrompt(String prompt) {
		this.prompt = prompt;
	}
	public String getContextCode() {
		return contextCode;
	}
	public void setContextCode(String contextCode) {
		this.contextCode = contextCode;
	}
	public String getSegmentCode() {
		return segmentCode;
	}
	public void setSegmentCode(String segmentCode) {
		this.segmentCode = segmentCode;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public String getSequenceNumber() {
		return sequenceNumber;
	}
	public void setSequenceNumber(String sequenceNumber) {
		this.sequenceNumber = sequenceNumber;
	}
	public String getValueSetCode() {
		return valueSetCode;
	}
	public void setValueSetCode(String valueSetCode) {
		this.valueSetCode = valueSetCode;
	}
	public String getValidationType() {
		return validationType;
	}
	public void setValidationType(String validationType) {
		this.validationType = validationType;
	}
	public String getValueDataType() {
		return valueDataType;
	}
	public void setValueDataType(String valueDataType) {
		this.valueDataType = valueDataType;
	}
	
}
